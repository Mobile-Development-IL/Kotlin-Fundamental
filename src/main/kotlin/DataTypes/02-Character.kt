package org.example.DataTypes

fun main() {
    // Char
    var grade: Char = 'A'
    println(grade)

    // Increment
    println("Grade : " + grade++)
    println("Grade : " + grade++)
    println("Grade : " + grade++)
    println("")
    // Decrement
    println("Grade : " + grade--)
    println("Grade : " + grade--)
    println("Grade : " + grade--)
}