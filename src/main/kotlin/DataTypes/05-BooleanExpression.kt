package org.example.DataTypes

fun main() {
    // Boolean Expression
    val x: Int = 50
    val y: Int = 24

    // More than
    println("x > y = " + (x > y))
    // Less than
    println("x < y = " + (x < y))
    // Greater than or equal to
    println("x >= y = " + (x >= y))
    // Less than or equal to
    println("x <= y = " + (x <= y))
    // Equal
    println("x == y = " + (x == y))
    // Not equal
    println("x != y = " + (x != y))

}